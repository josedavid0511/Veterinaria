    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            this.agregarMascota();
            Utilidad.lanzarAlerta(this, "La mascota ha sido agregada", "Registro de mascota", JOptionPane.INFORMATION_MESSAGE);
            Principal.actualizarListas();
            this.limpiarFormulario();
        } catch (Exception e) {
            Utilidad.lanzarAlerta(this, "Los datos del formulario son incorrectos, verifique y digite nuevamente", "Error dato numérico", JOptionPane.INFORMATION_MESSAGE);
        }
        

    }//GEN-LAST:event_btnGuardarActionPerformed

